#Sum of number
def cal_sum(a,b,c,d,e):                
    sum = a+b+c+d+e 
    print(sum)
    return sum
    
cal_sum(100,116,134,345,234)

#sub of number
def cal_sub(a,b):                
    sub = a-b
    print(sub)
    return sub
    
cal_sub(345,126)

#Multiplication of number
def cal_mul(a,b,c,d):                
    mul = a*b*c*d
    print(mul)
    return mul
    
cal_mul(100,116,134,345)

#Division of number
def cal_div(a,b):                
    div = a/b
    print(div)
    return div
    
